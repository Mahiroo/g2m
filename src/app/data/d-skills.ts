/**
 * スキル情報リスト初期化.
 */
export function initSkills(): _.Dictionary<ISkill> {
    const keys: string[] = _.map(master, values => values[0]);
    const values: ISkill[] = _.map(master, (values, index): ISkill => {
        const result: ISkill = {
            displayName: values[0],
            group: values[1],
            kind: 'スキル',
            name: values[0],
            seq: index + 1,
        };
        _.forEach(values.slice(2), obj => {
            for (const propKey in obj) {
                if (obj.hasOwnProperty(propKey)) {
                    result[propKey] = obj[propKey];
                }
            }
        });
        return result;
    });
    return _.object(keys, values);
}

